/*******************************************************************************
** 函数名称: UART_Rx_IDLECallback
** 功能描述: 串口接收空闲中断回调
** 参数说明: huart: 串口号
** 返回说明: None
** 创建人员: belen
** 创建日期: 2021-06-01
**------------------------------------------------------------------------------
** 修改人员:
** 修改日期:
** 修改描述:
**------------------------------------------------------------------------------
********************************************************************************/
 void UART_Rx_IDLECallback(UART_HandleTypeDef *huart)  
 {  
	uint32_t temp;  
	uint32_t tmp_flag=0; 
	 if (huart->Instance == USART1)
	 {
		 	tmp_flag =__HAL_UART_GET_FLAG(huart,UART_FLAG_IDLE);     //空闲中断,接收到一包数据后发生中断
			if((tmp_flag != RESET))
			{   
				__HAL_UART_CLEAR_IDLEFLAG(huart);  
				HAL_UART_DMAStop(huart);  
				temp = hdma_usart1_rx.Instance->CNDTR;  						//得到空余的数据长度
				rx1.RX_Size =  RX_LEN - temp; 											//得到已接收到的数据长度
				rx1.RX_flag=1;  
		 }  
	 }
	 if (huart->Instance == USART2)
	 {
		 tmp_flag =__HAL_UART_GET_FLAG(huart,UART_FLAG_IDLE); 
			if((tmp_flag != RESET))
			{   
				__HAL_UART_CLEAR_IDLEFLAG(huart);  
				HAL_UART_DMAStop(huart);  
				temp = hdma_usart2_rx.Instance->CNDTR;  
				rx2.RX_Size =  RX_LEN - temp;  
				rx2.RX_flag=1;  				
		 }  
	 }
}


/*******************************************************************************
** 函数名称: uart_Handle
** 功能描述: 串口处理
** 参数说明: huart: 串口号
**			 rx: 接收数据
** 返回说明: None
** 创建人员: belen
** 创建日期: 2021-06-01
**------------------------------------------------------------------------------
** 修改人员:
** 修改日期:
** 修改描述:
**------------------------------------------------------------------------------
********************************************************************************/
 void uart_Handle(UART_HandleTypeDef *huart,UART_DataTypeDef *rx)
   {
		 if(rx->RX_flag ==1)
			{  		
				HAL_UART_Transmit(huart ,rx->RX_pData, rx->RX_Size,200);
				memset(rx->RX_pData, 0, rx->RX_Size);			
				rx->RX_Size=0;
				rx->RX_flag=0;
			 }
			HAL_UART_Receive_DMA(huart,rx->RX_pData,RX_LEN );  
 }

 

